    private void jRadioButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton1ActionPerformed
        // TODO add your handling code here:
        //Activar y Desactivar el boton de trabajo
        if(jRadioButton1.isSelected()){
            jRadioButton1.setForeground(new java.awt.Color(0, 255, 0));
            Vector<Pedido> pedidos = Empresa.pedidos;
            for(int i=0;cant<3&&i<pedidos.size();i++){
                if(pedidos.get(i).enProceso==false){
                    CrearPedido(cant,pedidos.get(i));
                    cant++;
                }
                else
                    continue;
            }
            
        }
        else{
            jRadioButton1.setForeground(new java.awt.Color(255, 255, 255));
            while(cant>=1){
                jPanel2.remove(9);
                cant--;
            }
            
            jPanel2.repaint();
        }
    }//GEN-LAST:event_jRadioButton1ActionPerformed

