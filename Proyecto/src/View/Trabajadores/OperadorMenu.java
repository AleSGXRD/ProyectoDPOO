    public void CrearPedido(int posY,Pedido pedido){             
        javax.swing.JPanel jPanel3;         
        javax.swing.JButton jButton2;
        javax.swing.JComboBox<String> jComboBox1;
        javax.swing.JLabel jLabel1;
         
        jPanel3 = new javax.swing.JPanel();
        jComboBox1 = new javax.swing.JComboBox<>();
        jLabel1 = new javax.swing.JLabel();
        jButton2 = new javax.swing.JButton();
        jPanel3.setBackground(new java.awt.Color(255, 255, 255));
        jPanel3.setBorder(javax.swing.BorderFactory.createMatteBorder(2, 2, 2, 2, new java.awt.Color(0, 0, 0)));
        jPanel3.setLayout(new org.netbeans.lib.awtextra.AbsoluteLayout());

        jLabel1.setForeground(new java.awt.Color(0, 0, 0));
        jLabel1.setText(pedido.client.getNombre()+"-Direccion:"+pedido.client.getDireccion());
        jPanel3.add(jLabel1, new org.netbeans.lib.awtextra.AbsoluteConstraints(10, 0, 226, 70));

        jComboBox1.setBackground(new java.awt.Color(51, 51, 51));
                if(Empresa.trabajadores.taxistas.get(0).isTrabajando()==true){
                    jComboBox1.setForeground(Color.green);
                }
                else{
                    jComboBox1.setForeground(Color.red);
                }
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(nombres));
        jComboBox1.setFocusable(false);
        jComboBox1.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                if(Empresa.trabajadores.taxistas.get(jComboBox1.getSelectedIndex()).isTrabajando()==true){
                    jComboBox1.setForeground(Color.green);
                }
                else{
                    jComboBox1.setForeground(Color.red);
                }
            }
        });
        jPanel3.add(jComboBox1, new org.netbeans.lib.awtextra.AbsoluteConstraints(278, 20, 150, 30));

        jButton2.setBackground(new java.awt.Color(51, 51, 51));
        jButton2.setForeground(new java.awt.Color(255, 255, 255));
        jButton2.setText("Enviar Peticion");
        jButton2.setBorderPainted(false);
        jButton2.setFocusable(false);
        jButton2.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                try {
                    current.AsignarPedido(jComboBox1.getSelectedIndex(), pedido);
                    //Borrar
                    jPanel2.remove(9+posY);
                    //Empresa.pedidos.remove(posY);
                    cant--;
                    //ActualizarInterfaz
                    Actualizar();
                    //Repaint
                    jPanel2.repaint();
                } catch (Exception ex) {
                    Error.setText(ex.getMessage());
                }
            }
        });
        jPanel3.add(jButton2, new org.netbeans.lib.awtextra.AbsoluteConstraints(440, 10, 130, 50));

        jPanel2.add(jPanel3, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 70+(posY*90), 580, 70));

        pack();
    }
    
    public void Actualizar(){
        while(cant>=1){
            jPanel2.remove(9);
            cant--;
        }   
        Vector<Pedido> pedidos = Empresa.pedidos;
        for(int i=0;cant<3&&i<pedidos.size();i++){
            if(pedidos.get(i).enProceso==false){
                CrearPedido(cant,pedidos.get(i));
                cant++;
            }
            else
                continue;
        }
            
    }
